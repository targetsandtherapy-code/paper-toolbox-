def is_core_journal(journal_name: str) -> bool:
    """判断期刊是否为核心期刊（模糊匹配）"""
    if not journal_name:
        return False
    name = journal_name.strip()
    if name in CORE_JOURNALS:
        return True
    for core in CORE_JOURNALS:
        if core in name or name in core:
            return True
    return False
